// config.js
// Turns the editor state into a real ~/.tmux.conf string.
// Colors are INLINED as #RRGGBB hex (tmux does not expand $VAR in formats).
// The battery segment is INLINED too: no external script.
//
// tmux #(...) matches the first closing paren, so the battery command is kept
// paren-free: command substitution uses backticks, branching uses if/fi
// (never `case ... )`), and awk programs use braces only.

const ORDER = ["accent", "bg", "cur", "fg", "comment", "cyan", "green", "orange", "pink", "red", "yellow"];

// Nerd Font glyphs (FontAwesome + Devicons ranges), as JS unicode escapes so
// the source stays ASCII. generateConfig emits the real codepoints into the
// config, and render.js paints the same glyphs in the live preview.
export const NF = {
  bolt: "\uf0e7", // charging
  plug: "\uf1e6", // plugged in, not charging
  full: "\uf240",
  q3: "\uf241",
  half: "\uf242",
  q1: "\uf243",
  branch: "\ue725", // git branch (nf-dev-git_branch)
};

function block(title, lines) {
  return [`# --- ${title} ---`, ...lines, ""].join("\n");
}

// Inline battery command. Reads sysfs (no upower dependency).
// Uses backticks (paren-free, so tmux #() matches the real close paren) and
// echo (no single quotes), so the whole status-right can be single-quoted in
// the config, which stops tmux from expanding the $p/$s/$i shell variables.
function batteryCmd(state) {
  const b = "`";
  const cap = "cat /sys/class/power_supply/BAT0/capacity 2>/dev/null || cat /sys/class/power_supply/BAT1/capacity 2>/dev/null || echo 100";
  const st = "cat /sys/class/power_supply/BAT0/status 2>/dev/null || cat /sys/class/power_supply/BAT1/status 2>/dev/null";
  if (state.nerdfont) {
    const segs = [
      `p=${b}${cap}${b}`,
      `s=${b}${st}${b}`,
      `if [ $p -ge 75 ]; then i=${NF.full}; elif [ $p -ge 50 ]; then i=${NF.q3}; elif [ $p -ge 25 ]; then i=${NF.half}; else i=${NF.q1}; fi`,
      `if [ $s = Charging ]; then echo ${NF.bolt} $i $p%; elif [ $s = Full ]; then echo ${NF.plug} $i $p%; else echo $i $p%; fi`,
    ];
    return "#(" + segs.join("; ") + ")";
  }
  // Plain percentage, no icons.
  return "#(" + cap + ")%%";
}

export function generateConfig(state) {
  const P = state.palette;
  const f = state.features;
  const pos = state.statusPosition;
  const L = [];

  L.push("# ~/.tmux.conf");
  L.push("# Generated by samux: sane tmux configuration.");
  L.push("# https://nathabonfim59.github.io/samux/");
  L.push("#");
  L.push(`# Theme: ${state.themeName}`);
  L.push(`# Nerd Font icons: ${state.nerdfont ? "on (install a Nerd Font for the status bar glyphs)" : "off"}`);
  L.push("# Prefix is C-b. Reload after editing:  tmux source-file ~/.tmux.conf");
  L.push("");

  L.push(`# --- Palette: ${state.themeName} ---`);
  for (const r of ORDER) L.push(`# ${r.padEnd(8)} ${P[r]}`);
  L.push("");

  L.push(block("Core sane defaults", ["set -s escape-time 0"]));

  if (f.vim) L.push(block("VI copy mode", ["setw -g mode-keys vi"]));
  if (f.mouse) L.push(block("Mouse: click windows, resize panes, scroll", ["set -g mouse on"]));
  if (f.windows1) {
    L.push(block("Windows start at 1 and renumber on close", [
      "set -g base-index 1",
      "setw -g pane-base-index 1",
      "set -g renumber-windows on",
    ]));
  }
  if (f.altnum) {
    const binds = [1, 2, 3, 4, 5, 6, 7, 8, 9].map((n) => `bind -n M-${n} select-window -t ${n}`);
    L.push(block("Alt + number to switch windows (like a browser)", binds));
  }
  if (f.altnav) {
    L.push(block("Alt + h/l: prev/next window, Alt + j/k: swap window position", [
      "bind -n M-h select-window -t :-",
      "bind -n M-l select-window -t :+",
      "bind -n M-j swap-window -t :-",
      "bind -n M-k swap-window -t :+",
    ]));
  }
  if (f.passthrough) {
    L.push(block("Extended keys + passthrough (Claude Code compatibility)", [
      "set -g allow-passthrough on",
      "set -s extended-keys on",
      "set -as terminal-features 'xterm*:extkeys'",
    ]));
  }
  if (f.truecolor) {
    const term = state.term;
    L.push(block("True color", [
      `set -g default-terminal "${term}"`,
      `set -ag terminal-overrides ",${term}:RGB,*256col*:RGB"`,
    ]));
  }
  if (f.wayland) {
    L.push(block("Wayland: propagate env so clipboard and battery scripts work", [
      'set -g update-environment "DISPLAY WAYLAND_DISPLAY XDG_RUNTIME_DIR SSH_AUTH_SOCK"',
      'set-hook -g client-attached \'run-shell "tmux setenv -g WAYLAND_DISPLAY #{WAYLAND_DISPLAY}"\'',
    ]));
  }
  if (f.copy) {
    L.push(block("Copy selection to Wayland clipboard (y in copy mode)", [
      'bind -T copy-mode-vi y send -X copy-pipe-and-cancel "wl-copy"',
    ]));
  }
  if (f.bell) {
    L.push(block("Bell and activity indicator", [
      "set -g visual-bell off",
      "set -g bell-action other",
      "setw -g monitor-bell on",
    ]));
  }
  if (f.autorename) {
    L.push(block("Rename windows whose pane is running claude to its title", [
      'set-hook -g pane-title-changed \'if-shell "[ \\"#{pane_current_command}\\" = \\"claude\\" ]" "rename-window \\"#{pane_title}\\""\'',
    ]));
  }

  L.push(block("Pane borders", [
    `set -g pane-border-style "fg=${P.comment}"`,
    `set -g pane-active-border-style "fg=${P.accent}"`,
  ]));

  L.push(block("Message prompt and copy mode highlight", [
    `set -g message-style "fg=${P.fg},bg=${P.cur}"`,
    `setw -g mode-style "fg=${P.bg},bg=${P.accent}"`,
  ]));

  L.push(block("Status bar", [
    `set -g status-style "fg=${P.fg},bg=${P.bg}"`,
    "set -g status-interval 5",
    `set -g status-position ${pos}`,
    "set -g status-left-length 40",
    `set -g status-left '${statusLeft(state, P)}'`,
    "set -g status-right-length 120",
    `set -g status-right '${statusRight(state, P)}'`,
    `setw -g window-status-format "#[fg=${P.comment},bg=${P.bg}]  #I:#W#F  "`,
    `setw -g window-status-current-format "#[fg=${P.fg},bg=${P.cur},bold]  #I:#W  "`,
    'setw -g window-status-separator ""',
  ]));

  return L.join("\n") + "\n";
}

function statusLeft(state, P) {
  const parts = [];
  if (state.left.session) {
    parts.push(`#[fg=${P.bg},bg=${P.accent},bold] #S `);
    parts.push(`#[fg=${P.accent},bg=${P.bg},nobold] `);
  }
  if (state.left.userhost) {
    parts.push(`#[fg=${P.comment}] #(whoami)@#H `);
  }
  if (parts.length === 0) parts.push(`#[fg=${P.comment}] samux `);
  return parts.join("");
}

function statusRight(state, P) {
  const parts = [];
  if (state.right.git) {
    const gitLabel = state.nerdfont ? `${NF.branch} ` : "git:";
    parts.push(`#[fg=${P.pink}] ${gitLabel}#(git rev-parse --abbrev-ref HEAD 2>/dev/null) `);
  }
  if (state.right.battery) parts.push(`#[fg=${P.comment}]${batteryCmd(state)} `);
  if (state.right.date) parts.push(`#[fg=${P.comment}]%a %d %b `);
  if (state.right.clock) {
    const fmt = state.clock24 ? "%H:%M" : "%I:%M";
    parts.push(`#[fg=${P.bg},bg=${P.accent},bold] ${fmt} `);
  }
  return parts.join("");
}
